public class NumberFormat {
    public static void main(String[] args) {
        System.out.printf("%,d\n",1000);
        System.out.printf("%,d\n",1000);
        System.out.printf("%2.2f\n",1090.9032);

        System.out.printf("%5.3ef\n",1090.9999);
        System.out.printf("%4.3ef\n",1090.9999);
    }
    
}
